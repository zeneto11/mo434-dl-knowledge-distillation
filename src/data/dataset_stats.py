"""
Compute and save dataset statistics for FGVC-Aircraft and Food-101.

Outputs:
  results/dataset_stats/aircraft.json
  results/dataset_stats/food101.json
  results/reports/dataset_stats.md

Usage:
  python -m src.data.dataset_stats --root data --output-dir results/dataset_stats
"""
from __future__ import annotations

import argparse
import json
from collections import Counter
from pathlib import Path

from torchvision import datasets

IMAGENET_MEAN = (0.485, 0.456, 0.406)
IMAGENET_STD  = (0.229, 0.224, 0.225)


def _get_targets(dataset) -> list[int]:
    """Extract integer targets from any torchvision dataset."""
    if hasattr(dataset, "_labels"):
        return list(dataset._labels)
    if hasattr(dataset, "targets"):
        return list(dataset.targets)
    return [int(dataset[i][1]) for i in range(len(dataset))]


def _split_stats(targets: list[int]) -> dict:
    counts = Counter(targets)
    sizes = list(counts.values())
    return {
        "size": len(targets),
        "num_classes": len(counts),
        "min_per_class": min(sizes),
        "max_per_class": max(sizes),
        "mean_per_class": round(sum(sizes) / len(sizes), 2),
        "imbalance_ratio": round(max(sizes) / max(min(sizes), 1), 3),
    }


def aircraft_stats(root: Path) -> dict:
    splits: dict[str, dict] = {}
    for split in ("train", "val", "test"):
        ds = datasets.FGVCAircraft(
            root=str(root),
            split=split,
            annotation_level="variant",
            download=True,
        )
        splits[split] = _split_stats(_get_targets(ds))
    return {
        "dataset": "FGVC-Aircraft",
        "key": "aircraft",
        "num_classes": 100,
        "annotation_level": "variant",
        "image_size_assumption": 224,
        "normalization": {"mean": list(IMAGENET_MEAN), "std": list(IMAGENET_STD)},
        "splits": splits,
    }


def food101_stats(root: Path) -> dict:
    splits: dict[str, dict] = {}

    train_full = datasets.Food101(root=str(root), split="train", download=True)
    test_full  = datasets.Food101(root=str(root), split="test",  download=True)

    train_targets = _get_targets(train_full)
    test_targets  = _get_targets(test_full)

    # 10% of train is used as validation (indices divisible by 10)
    val_indices   = [i for i in range(len(train_targets)) if i % 10 == 0]
    train_indices = [i for i in range(len(train_targets)) if i % 10 != 0]

    splits["train"] = _split_stats([train_targets[i] for i in train_indices])
    splits["val"]   = _split_stats([train_targets[i] for i in val_indices])
    splits["test"]  = _split_stats(test_targets)

    return {
        "dataset": "Food-101",
        "key": "food101",
        "num_classes": 101,
        "val_strategy": "every-10th train sample",
        "image_size_assumption": 224,
        "normalization": {"mean": list(IMAGENET_MEAN), "std": list(IMAGENET_STD)},
        "splits": splits,
    }


def write_markdown_report(stats_list: list[dict], output: Path) -> None:
    output.parent.mkdir(parents=True, exist_ok=True)
    lines = [
        "# Dataset Statistics",
        "",
        "Generated by `src.data.dataset_stats`.",
        "",
    ]
    for stats in stats_list:
        lines += [f"## {stats['dataset']}", ""]
        lines += [f"- **Classes:** {stats['num_classes']}"]
        lines += [f"- **Image size assumption:** {stats['image_size_assumption']} × {stats['image_size_assumption']}"]
        if "annotation_level" in stats:
            lines += [f"- **Annotation level:** {stats['annotation_level']}"]
        if "val_strategy" in stats:
            lines += [f"- **Validation strategy:** {stats['val_strategy']}"]
        lines += ["", "| Split | Size | Num classes | Min/class | Max/class | Mean/class | Imbalance ratio |",
                  "|-------|------|-------------|-----------|-----------|------------|-----------------|"]
        for split, s in stats["splits"].items():
            lines.append(
                f"| {split} | {s['size']:,} | {s['num_classes']} "
                f"| {s['min_per_class']} | {s['max_per_class']} "
                f"| {s['mean_per_class']:.1f} | {s['imbalance_ratio']:.2f} |"
            )
        lines += [""]
    output.write_text("\n".join(lines), encoding="utf-8")


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--root", default="data")
    parser.add_argument("--output-dir", default="results/dataset_stats")
    parser.add_argument("--report", default="results/reports/dataset_stats.md")
    args = parser.parse_args()

    root = Path(args.root)
    out_dir = Path(args.output_dir)
    out_dir.mkdir(parents=True, exist_ok=True)

    print("Computing Aircraft stats...")
    ac = aircraft_stats(root)
    (out_dir / "aircraft.json").write_text(
        json.dumps(ac, indent=2) + "\n", encoding="utf-8")
    print(f"  Saved {out_dir}/aircraft.json")

    print("Computing Food-101 stats...")
    fo = food101_stats(root)
    (out_dir / "food101.json").write_text(
        json.dumps(fo, indent=2) + "\n", encoding="utf-8")
    print(f"  Saved {out_dir}/food101.json")

    write_markdown_report([ac, fo], Path(args.report))
    print(f"  Saved {args.report}")


if __name__ == "__main__":
    main()
